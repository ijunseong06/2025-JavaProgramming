public class IntegerLiteralExample {
    public static void main(String[] args) {
        //Literal Example
        //2진수 : 0b 또는 0B로 시작
        //8진수 : 0으로 시작
        //10진수 : 소수점 없는 숫자로 작성
        //16진수 : 0x또는 0X로 시작
        int var1 = 0b1011;
        int var2 = 0206;
        int var3 = 365;
        int var4 = 0xB3;

        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var3);
        System.out.println(var4);
    }
}
